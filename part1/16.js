// switch case
let day = 1;

switch(day){
    case 0:
        console.log("Sunday");
    case 1:
        console.log("Monday");
        break;
    case 2:
        console.log("Tuesday");
        break;
    case 3:
        console.log("Wednesday");
        break;
    case 4:
        console.log("Thursday");
        break;
    case 5:
        console.log("Firday");
        break; 
    case 6:
        console.log("Saturday");
        break;
    default:
        console.log("Invalid Day");            
}

